
package com.array;

public class SingleDimentionalArray {
    public static void main(String[] args) {
        // Single dimentional Array, one [] sing
        int q [] = {40, 55, 56, 61, 53, 73};
        // Index     0   1   2   3   4   5 
        for (int j : q){
            System.out.print(j +" ");
           
        }
        System.out.println();
    }
    
}
