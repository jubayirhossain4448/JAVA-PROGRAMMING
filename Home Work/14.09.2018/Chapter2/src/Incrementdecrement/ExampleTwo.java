public class ExampleTwo {
    public static void main(String[] args) {
        int a = 6;
        int b = a++;
        System.out.println("The value of 'b' is: " + b);
        
        a = 6;
        b = ++a;
        System.out.println("The value of a and b is: " +a + " and "  + b);
    }
    
}
