package WhileLoopEx;

public class WhileLoopEx100 {

    public static void main(String[] args) {
        int a = 0;
        int sum = 0;
        while (a < 10) {
            sum += a;
            System.out.println(sum);
            a++;

        }
    }

    public void display() {
        int count = 0;
        while (count < 100) {

            count++;
        }
        System.out.println("Welcome to Java");
    }
}
